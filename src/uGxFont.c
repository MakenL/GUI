/** @brief Функция возвращает высоту символов в пикселях для заданного шрифта
    @param[in] pFont указатель на шрифт типа uGxFont_t
    @return высота шрифта в пикселях
 */
uint16_t uGx_GetFontHeight(uGxFont_t *pFont)
{
  uint16_t FontHeight = 0;

  if (pFont != NULL)
  {
    FontHeight = pFont->Height;
  }
  return FontHeight;
}

/** @brief Функция возвращает ширину строки в пикселях для заданного шрифта
    @param[in] pString указатель на строку
    @param[in] pFont указатель на шрифт типа uGxFont_t
    @return    ширина строки в пикселях
 */
uint16_t uGx_GetStrWeight(char *pString, uGxFont_t *pFont)
{
  uint16_t StrLenght = 0;

  if ((pString != NULL) && (pFont != NULL))
  {
    while (*pString)
    {
			if ((*pString >= pFont->StartChar) && (*pString <= pFont->EndChar))
			{
				StrLenght += pFont->Descriptor[(*pString - pFont->StartChar) * 2];
			}
      pString++;
    }
  }
  return StrLenght;
}

